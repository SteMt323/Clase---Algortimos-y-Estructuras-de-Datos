"""
Elabore un programa que solicite dos números enteros y presente como resultados
cada una de las respuestas de todos los operadores matemáticos
"""
num1 = int(input("Ingrese el primer número: "))
num2 = int(input("Ingrese el segundo número: "))

print(f"La suma de {num1} + {num2} es: \n{num1 + num2}\n\n")
print(f"La resta de {num1} - {num2} es: \n{num1 - num2}\n\n")
print(f"La multiplicación de {num1} * {num2} es: \n{num1 * num2}\n\n")
print(f"La división de {num1} / {num2} es: \n{num1 / num2}\n\n")
print(f"El módulo de {num1} % {num2} es: \n{num1 % num2}\n\n")
print(f"La división entera de {num1} // {num2} es: \n{num1 // num2}\n\n")
print(f"La potencia de {num1} ** {num2} es: \n{num1 ** num2}\n\n")

